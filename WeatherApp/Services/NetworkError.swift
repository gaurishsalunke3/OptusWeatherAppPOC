import Foundation

enum NetworkError: Error {
    case badURL(FetchError)
    case requestFailed(FetchError)
    case unknown(FetchError)
}

enum FetchError: Error {
    case error(String)
    
    var localizedDescription: String {
        switch self {
        case .error(let message):
            return message
        }
    }
}
